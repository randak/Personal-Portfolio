     * @param     mixed $key Primary key to use for the query
     *
     * @return ProjectCategoryQuery The current query, for fluid interface
     */
    public function filterByPrimaryKey($key)
    {
        $this->addUsingAlias(ProjectCategoryPeer::PROJECT_ID, $key[0], Criteria::EQUAL);
        $this->addUsingAlias(ProjectCategoryPeer::CATEGORY_ID, $key[1], Criteria::EQUAL);

        return $this;
    }

    /**
     * Filter the query by a list of primary keys
     *
     * @param     array $keys The list of primary key to use for the query
     *
     * @return ProjectCategoryQuery The current query, for fluid interface
     */
    public function filterByPrimaryKeys($keys)
    {
        if (empty($keys)) {
            return $this->add(null, '1<>1', Criteria::CUSTOM);
        }
        foreach ($keys as $key) {
            $cton0 = $this->getNewCriterion(ProjectCategoryPeer::PROJECT_ID, $key[0], Criteria::EQUAL);
            $cton1 = $this->getNewCriterion(ProjectCategoryPeer::CATEGORY_ID, $key[1], Criteria::EQUAL);
            $cton0->addAnd($cton1);
            $this->addOr($cton0);
        }

        return $this;
    }

